// Arithmetic Operators
const sum = 5 + 3; // Addition
const diff = 10 - 2; // Subtraction
const p = 4 * 2; // Multiplication
const q = 8 / 2; // Division
const exp = 2**3
console.log(sum, diff, p, q,exp);

// Assignment Operators

let n = 10;
n += 5;
n *= 2;
console.log(n);

// Comparison Operator

console.log(NaN == NaN);
console.log(0 == false);   
console.log(0 == null);

console.log(NaN === NaN);   
console.log(0 === false);   
console.log(0 === null);


//  Logical Operator
a = 10
b = 20
console.log(a && b); // Logical AND
console.log(a || b); // Logical OR

//  Bitwise Operators
const res = 5 & 1; // Bitwise AND
console.log(res);

// Ternary Operator

const age = 18;
const status = age >= 18 ? "Adult" : "Minor";
console.log(status);

