// for loop 
console.log("Using for loop");

for(let i = 1;i<=10;i++){
    console.log(i)
}

// while loop
console.log("Using the while loop");
let i = 1
while(i<=10){
    console.log(i);
    i++;
}

// do-while loop 
console.log("Using the do-while loop");
i = 1;
do {
    console.log(i);
    i++;
} while (i <= 10);